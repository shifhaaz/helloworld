
public class QuotientAndReminder {

	public static void main(String[] args) {
		int Divisor=4;
		int Dividend = 25;
		int reminder= Dividend % Divisor;
		int quotient=	Dividend / Divisor;
		System.out.println("This is the value of Reminder:"+ reminder);
		System.out.println("This is the value of quotient:"+ quotient);
		
	}

}
