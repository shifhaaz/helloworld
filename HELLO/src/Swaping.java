
public class Swaping {

	public static void main(String[] args) {
		float n1=2.5f;
		float n2=-3.5f;
		 System.out.println("Value before swap of n1\n"+n1);
		 System.out.println("Value before swap of n2\n"+n2);
		 
		 float temp=n1;
		 n1=n2;
		 n2=temp;
		 
		 System.out.println("Value after swap of n1\n"+n1);
		 System.out.println("Value after swap of n2\n"+n2);
		 
	}

}
